void	verLine(t_info *info, int x, int y1, int y2, int color)
{
	int	y;

	y = 0;
	while (y <= y1)
	{
		mlx_pixel_put(info->mlx, info->win, x, y,  0xFFFFFF);
		y++;
	}
	while (y <= y2)
	{
		mlx_pixel_put(info->mlx, info->win, x, y, color);
		y++;
	}
	while (y <= height)
	{
		mlx_pixel_put(info->mlx, info->win, x, y,  0xFFFFFF);
		y++;
	}
}


void draw_line(t_info *info, int x1, int y1, int x2, int y2)
{
    int dx = abs(x2 - x1);
    int dy = abs(y2 - y1);
    int sx = x1 < x2 ? 1 : -1;
    int sy = y1 < y2 ? 1 : -1;
    int err = (dx > dy ? dx : -dy) / 2;
    int e2;

    while (1)
    {
        mlx_pixel_put(info->mlx, info->win, x1, y1, 0x0000FF);
        if (x1 == x2 && y1 == y2)
            break;
        e2 = err;
        if (e2 > -dx)
        {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dy)
        {
            err += dx;
            y1 += sy;
        }
    }
}

void draw_map(t_info *info)
{
    for (int i = 0; i < mapHeight; i++)
    {
        for (int j = 0; j < mapWidth; j++)
        {
            if (worldMap[i][j] != 0)
            {
                int x = j * TILE_SIZE;
                int y = i * TILE_SIZE;
                for (int k = 0; k < TILE_SIZE; k++)
                {
                    for (int l = 0; l < TILE_SIZE; l++)
                    {
                        mlx_pixel_put(info->mlx, info->win, x + k, y + l, 0xFFFFFF);
                    }
                }
            }
			else	
			{
                int x = j * TILE_SIZE;
                int y = i * TILE_SIZE;
                for (int k = 0; k < TILE_SIZE; k++)
                {
                    for (int l = 0; l < TILE_SIZE; l++)
                    {
                        mlx_pixel_put(info->mlx, info->win, x + k, y + l,0x000000);
                    }
                }
            }

        }
    }
}

void	calc(t_info *info)
{
	int	x;

	x = 0;


	draw_map(info);
	while (x < width)
	{
		double cameraX = 2 * x / (double)width - 1;
		//width -> -1 ~ 1
		double rayDirX = info->dirX + info->planeX * cameraX;
		double rayDirY = info->dirY + info->planeY * cameraX;
	
// 	이를 활용해서 광선의 방향을 계산할 것입니다.
// rayDirX, rayDirY 는 광선의 방향벡터 입니다.
// 앞서 설명한 것과 같이 광선의 방향은 ( 방향벡터 ) + ( 카메라평면 x 배수 ) 로 구할 수 있습니다. 벡터 x, y에 대해 각각 이 계산을 해줍니다.
		
		int mapX = (int)info->posX;
		int mapY = (int)info->posY;

		//length of ray from current position to next x or y-side
		double sideDistX;
		double sideDistY;
		
		 //length of ray from one x or y-side to next x or y-side
		double deltaDistX = fabs(1 / rayDirX);
		double deltaDistY = fabs(1 / rayDirY);
		double perpWallDist;
		
		//what direction to step in x or y-direction (either +1 or -1)
		int stepX;
		int stepY;
		
		int hit = 0; //was there a wall hit?
		int side; //was a NS or a EW wall hit?

		if (rayDirX < 0)
		{
			stepX = -1;
			sideDistX = (info->posX - mapX) * deltaDistX;
		}
		else
		{
			stepX = 1;
			sideDistX = (mapX + 1.0 - info->posX) * deltaDistX;
		}
		if (rayDirY < 0)
		{
			stepY = -1;
			sideDistY = (info->posY - mapY) * deltaDistY;
		}
		else
		{
			stepY = 1;
			sideDistY = (mapY + 1.0 - info->posY) * deltaDistY;
		}

		while (hit == 0)
		{
			//jump to next map square, OR in x-direction, OR in y-direction
			if (sideDistX < sideDistY)
			{
				sideDistX += deltaDistX;
				mapX += stepX;
				side = 0;
			}
			else
			{
				sideDistY += deltaDistY;
				mapY += stepY;
				side = 1;
			}
			//Check if ray has hit a wall
			if (worldMap[mapX][mapY] > 0) hit = 1;
		}
		if (side == 0)
			perpWallDist = (mapX - info->posX + (1 - stepX) / 2) / rayDirX;
		else
			perpWallDist = (mapY - info->posY + (1 - stepY) / 2) / rayDirY;

		//Calculate height of line to draw on screen
		int lineHeight = (int)(height / perpWallDist);

		//calculate lowest and highest pixel to fill in current stripe
		int drawStart = -lineHeight / 2 + height / 2;
		if(drawStart < 0)
			drawStart = 0;
		int drawEnd = lineHeight / 2 + height / 2;
		if(drawEnd >= height)
			drawEnd = height - 1;

		int	color;
		if (worldMap[mapY][mapX] == 1)
			color = 0xFF0000;
		else if (worldMap[mapY][mapX] == 2)
			color = 0x00FF00;
		else if (worldMap[mapY][mapX] == 3)
			color = 0x0000FF;
		else if (worldMap[mapY][mapX] == 4)
			color = 0xFFFFFF;
		else
			color = 0xFFFF00;
		
		if (side == 1)
			color = color / 2;

		// int TILE_SIZE = 15;

		draw_line(info, TILE_SIZE * info->posX, TILE_SIZE * info->posY, TILE_SIZE * mapX, TILE_SIZE * mapY);
		// verLine(info, x, drawStart, drawEnd, color);
		
		x++;
	}
}



