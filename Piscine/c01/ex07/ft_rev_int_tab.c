void	ft_rev_int_tab(int *tab, int size)
{
	int	n;
	int	em;

	n = 0;
	while (n < (size / 2))
	{
		em = tab[n];
		tab[n] = tab [size - n - 1];
		tab[size - n - 1] = em;
		n ++;
	}
}
