int	ft_sqrt(int nb)
{
	long long	i;

	i = 2;
	if (nb < 1)
		return (0);
	else if (nb == 1)
		return (1);
	while (i * i <= nb)
	{	
		if (i * i == nb)
			return (i);
		i ++;
	}
	return (0);
}
